def add_numbers(a: int, b: int) -> int:
    """
    Add two numbers.
    
    Args:
        a (int): First number.
        b (int): Second number.
        
    Returns:
        int: The sum of the two numbers.
    """
    return a + b

if __name__ == "__main__":
    # Example usage
    print(add_numbers(5, 3))
